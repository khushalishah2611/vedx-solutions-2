export const getBlogBySlug = (slug) => blogPosts.find((post) => post.slug === slug);

export const getRelatedPosts = (slug, category, tags = []) => {
  const normalisedTags = tags.map((tag) => tag.toLowerCase());

  const scoredPosts = blogPosts
    .filter((post) => post.slug !== slug)
    .map((post) => {
      const sameCategory = post.category === category;
      const commonTags = post.tags?.filter((tag) => normalisedTags.includes(tag.toLowerCase())) ?? [];

      return {
        post,
        score: (sameCategory ? 2 : 0) + commonTags.length
      };
    })
    .sort((a, b) => b.score - a.score);

  const ranked = scoredPosts.filter(({ score }) => score > 0).map(({ post }) => post);

  if (ranked.length < 3) {
    const fallback = blogPosts.filter((post) => post.slug !== slug);
    fallback.forEach((post) => {
      if (!ranked.includes(post) && ranked.length < 3) {
        ranked.push(post);
      }
    });
  }

  return ranked.slice(0, 3);
};
